        prescripcionFinal.setCercaCilindroI(fldCercaCilindroI.getText());
        prescripcionFinal.setCercaEjeD(fldCercaEjeD.getText());
        prescripcionFinal.setCercaEjeI(fldCercaEjeI.getText());
